def multiply(a: int, b: int) -> int:
    """Multiply `a` and `b`

    Args:
        a: First number
        b: Second number
    """
    return a * b


def add(a: int, b: int) -> int:
    """Add `a` and `b`

    Args:
        a: First number
        b: Second number
    """
    return a + b


def divide(a: int, b: int) -> int:
    """Divide `a` and `b`

    Args:
        a: First number
        b: Second number
    """
    return a / b

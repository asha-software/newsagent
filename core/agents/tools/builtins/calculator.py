def add(a: float, b: float) -> float:
    """Add `a` and `b`

    Args:
        a: First number
        b: Second number

    Returns:
        The sum of `a` and `b`
    """
    return a + b


def multiply(a: float, b: float) -> float:
    """Multiply `a` and `b`

    Args:
        a: First number
        b: Second number

    Returns:
        The product of `a` and `b`
    """
    return a * b
